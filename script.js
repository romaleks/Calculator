function addNumbers(numOne, numTwo) {
   return numOne + numTwo;
}

function subtractNumbers(numOne, numTwo) {
   return numOne - numTwo;
}

function multiplyNumbers(numOne, numTwo) {
   return numOne * numTwo;
}

function divideNumbers(numOne, numTwo) {
   return numOne / numTwo;
}

function changePosNeg(number) {
   return -number;
}

